package assignment2_refactored.furniture.accessory;

/**
 * Represents a wall fixture.
 */
public class WallFixture extends AbstractAccessory {
  /**
   * Inits a wall fixture.
   *
   * @param num is the num of wall fixture needed in a furniture.part.
   */
  public WallFixture(int num) {
    setNum(num);
  }

  @Override
  public int hashCode() {
    return super.hashCode();
  }

  @Override
  public boolean equals(Object obj) {
    return super.equals(obj);
  }

  @Override
  public String toString() {
    return super.toString();
  }
}
